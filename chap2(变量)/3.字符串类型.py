# 定义时，可以使用单引号，双引号和三引号，
# 三引号可以在不同的行进行定义输出，而单引号，双引号不能
str1 = 'cdcddeff' #单引号
str2 = "dddccdcc" #双引号

#三引号
str3 = '''cdcddd
cdsdcsdcd'''
str4 = """xdcddcdd
dccddc"""

#输出
print(str1,type(str1))
print(str2,type(str2))
print(str3,type(str3))
print(str4,type(str4))
