print(1 + 1) # 加法运算符
print(1 - 1) # 减法运算符
print(1 * 1) # 乘法运算符
print(1 / 1) # 除法运算符

print(11 // 2) # 整除运算,当两个数一正一负时向下取整
print(11 // -2)
print(-11 // 2)

print(11 % 2) # 取模运算，当两个数一正一负时遵循公式   被除数 - 除数 * 商
print(11 % -2) # 11 - (- 2) * (-6) = -1
print(-11 % 2) # (-11) - 2 * (-6) = 1

print(3 ** 3) # 幂运算,3的3次方