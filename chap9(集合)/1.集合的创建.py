# 集合也是采用哈希函数映射的方式来生成的，相当于没有值的字典，只有键，键必须是唯一的，如果键相同，后边儿的会覆盖前边的
'''1.使用{}来定义集合'''
s = {'fdc','cdd',98}
print(s,type(s)) # {'fdc', 98, 'cdd'} <class 'set'>

'''2.使用set()内置函数来创建'''
s2 = set({10,20})
print(s2) # {10, 20} 键可以是任意类型

'''3.创建空的集合'''
'''不能使用s = {}的方式创建新的集合，会被默认的认为是字典的类型'''
s3 = set()
print(s3)
