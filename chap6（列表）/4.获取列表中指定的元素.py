'''可以使用索引获取列表中的元素，可以使用正向索引也可以使用逆向索引'''
lst = ['hello','world',988]

# 获取元素'world'
print(lst[1])  # 正向索引,从左向右0开始
print(lst[-2]) # 逆向索引,从右向左-1开始