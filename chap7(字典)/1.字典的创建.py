# 字典：存放的是键值对（通过哈希函数查找各个键对应的值）
'''1.使用{ }创建字典'''
score = {'张三':90,'李四':100,'王五':99}
print(score) # {'张三': 90, '李四': 100, '王五': 99}

'''2.使用内置函数dict()进行创建'''
stu = dict(姓名='张三',年龄=25)
print(stu) # {'姓名': '张三', '年龄': 25}